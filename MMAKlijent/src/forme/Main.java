    private void popuniKlijente() {
        try {
            ArrayList<Klijent> klijenti = KlijentskiKontroler.getInstance().getAllKlijent();

            cmbKlijent.removeAllItems();

            for (Klijent klijent : klijenti) {
                cmbKlijent.addItem(klijent);
            }

        } catch (Exception ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void popuniBorce() {
        try {
            ArrayList<Borac> borci = KlijentskiKontroler.getInstance().getAllBorac();

            cmbPrviBorac.removeAllItems();
            cmbDrugiBorac.removeAllItems();

            for (Borac borac : borci) {
                cmbPrviBorac.addItem(borac);
                cmbDrugiBorac.addItem(borac);
            }

        } catch (Exception ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void popuniGradove() {
        try {
            ArrayList<Grad> gradovi = KlijentskiKontroler.getInstance().getAllGrad();

            cmbGrad.removeAllItems();

            for (Grad grad : gradovi) {
                cmbGrad.addItem(grad);
            }

        } catch (Exception ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void refreshTable() {
        ModelTabeleMeceva tm = (ModelTabeleMeceva) tblMecevi.getModel();
        tm.refreshTable();
    }

