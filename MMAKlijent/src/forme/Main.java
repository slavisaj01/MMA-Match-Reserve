    private void btnDodajKartuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajKartuActionPerformed

        if (txtSediste.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Morate uneti sediste!");
            return;
        }

        Klijent klijent = (Klijent) cmbKlijent.getSelectedItem();
        String sediste = txtSediste.getText();

        Karta k = new Karta(null, klijent, sediste);

        ModelTabeleKarte tm = (ModelTabeleKarte) tblKarte.getModel();

        if (tm.postojiSediste(sediste)) {
            JOptionPane.showMessageDialog(this, "Ovo sediste je vec popunjeno!");
            return;
        }

        if (tm.postojiKlijent(klijent)) {
            JOptionPane.showMessageDialog(this, "Vec ste uneli ovog klijenta!");
            return;
        }

        tm.dodajKartu(k);

    }//GEN-LAST:event_btnDodajKartuActionPerformed

